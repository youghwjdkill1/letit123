number = 4  # 1 ~ 5

if number == 1:
    print('number는 1입니다')
elif number == 2:
    print('number는 2입니다')
elif number == 3:
    print('number는 3입니다')
elif number == 4:
    print('number는 4입니다')
elif number == 5:
    print('number5는 5입니다')
else:
    print('number 1 ~ 5는 아니에요')
