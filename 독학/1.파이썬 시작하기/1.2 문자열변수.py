# 이번에는 문자열 변수에 대하여 알아볼건데
# 처음에 배운 변수는 숫자를 넣어서 변수를 만들었는데
# 이번에는 문자열을 넣어서 만들어 볼것입니다
a = 'pig'
# a 는 pig와 같다고 표현한건데
# pig 앞하고 뒤에 꼭 따움표를 붙여줘야 합니다
# 안붙이면 컴퓨터가 pig도 변수라고 착각하기 떄문입니다
b = 'dad'
# b라는 변수에 dad라는 문자열을 넣었습니다
# a와b를 출력하면 어덯게 될까요?
print(a + b) #pigdad
# pigdad 라는 값이 나오네요
# 이것을 해결하기 위해서는 가장 간단한 방법인
# a 와 b 사이에 공배을 넣는것입니다
print(a + ' ' + b) # pig dad
# pig dad 라는 값이 나오는걸 볼수있습니다
